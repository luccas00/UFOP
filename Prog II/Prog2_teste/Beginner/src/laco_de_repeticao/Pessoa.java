package laco_de_repeticao;

public class Pessoa {
	
	private double peso;
	private int idade;
	
	public void setPeso(double peso) {
		this.peso = peso;
	}
	
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	public double getPeso() {
		return this.peso;
	}
	
	public int getIdade() {
		return this.idade;
	}
	
}
